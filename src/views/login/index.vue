<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" autocomplete="on" label-position="left">
      <img :src="m_login_logo" alt="" width="auto" height="100px">
      <div class="title-container">
        <h3 class="title">
          {{ $t('login.title') }}
        </h3>
        <lang-select class="set-language" />
      </div>
      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="username"
          v-model="loginForm.username"
          :placeholder="$t('login.username')"
          name="username"
          type="text"
          tabindex="1"
          autocomplete="on"
        />
      </el-form-item>
      <el-tooltip v-model="capsTooltip" content="Caps lock is On" placement="right" manual>
        <el-form-item prop="password">
          <span class="svg-container">
            <svg-icon icon-class="password" />
          </span>
          <el-input
            :key="passwordType"
            ref="password"
            v-model="loginForm.password"
            :type="passwordType"
            :placeholder="$t('login.password')"
            name="password"
            tabindex="2"
            autocomplete="on"
            @keyup.native="checkCapslock"
            @blur="capsTooltip = false"
            @keyup.enter.native="handleLogin"
          />
          <span class="show-pwd" @click="showPwd">
            <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
          </span>
        </el-form-item>
      </el-tooltip>
      <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">
        {{ $t('login.logIn') }}
      </el-button>
      <el-checkbox v-model="checked">自动登录</el-checkbox>
    </el-form>
  </div>
</template>

<script>
import { setAutoLogin, setHtInfo, getHtInfo, getAutoLogin } from '@/utils/auth'
import { validUsername } from '@/utils/validate'
import LangSelect from '@/components/LangSelect'
import AES from '@/api/aes'
export default {
  name: 'Login',
  components: { LangSelect },
  data() {
    const validateUsername = (rule, value, callback) => {
      if (!validUsername(value)) {
        callback(new Error('Please enter the correct user name'))
      } else {
        callback()
      }
    }
    const validatePassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('The password can not be less than 6 digits'))
      } else {
        callback()
      }
    }
    return {
      loginForm: {
        username: 'test@admin',
        password: '111111'
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur', validator: validateUsername }],
        password: [{ required: true, trigger: 'blur', validator: validatePassword }]
      },
      passwordType: 'password',
      capsTooltip: false,
      loading: false,
      showDialog: false,
      redirect: undefined,
      otherQuery: {},
      checked: true,
      m_login_logo: require('../../assets/images/m_login_logo.png')
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        const query = route.query
        if (query) {
          this.redirect = query.redirect
          this.otherQuery = this.getOtherQuery(query)
        }
      },
      immediate: true
    }
  },
  created() {
    // window.addEventListener('storage', this.afterQRScan)
    if (localStorage.login_info && getAutoLogin()) {
      const info = getHtInfo()
      this.localLogin(JSON.parse(info))
    } else {
      localStorage.clear()
    }
  },
  mounted() {
    if (this.loginForm.username === '') {
      this.$refs.username.focus()
    } else if (this.loginForm.password === '') {
      this.$refs.password.focus()
    }
    // 设置登录时的，系统默认语言
    this.$i18n.locale = 'zh'
    this.$store.dispatch('app/setLanguage', 'zh')
  },
  destroyed() {
    // window.removeEventListener('storage', this.afterQRScan)
  },
  methods: {
    checkCapslock(e) {
      const { key } = e
      this.capsTooltip = key && key.length === 1 && (key >= 'A' && key <= 'Z')
    },
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          const sendStr = {
            router: 'Login',
            JsonData: { name: this.loginForm.username, password: this.$md5(this.loginForm.password), ip: '', terminal: 'pc' }
          }
          // console.log('sendStr ', sendStr)
          this.$store.dispatch('user/login', sendStr)
            .then(() => {
              this.$pomelo.conn((err, res) => {
                if (err) console.error(err)
                if (res.code === 200) {
                  const en = this.$Global.en
                  this.$Global.Pass = this.loginForm.password
                  const ht_info = { account: this.loginForm.username, password: this.loginForm.password }
                  if (this.checked) {
                    setHtInfo(AES.encrypt(JSON.stringify(ht_info), en))
                    setAutoLogin('true')
                  }
                  this.$router.push({ path: this.redirect || '/', query: this.otherQuery })
                  this.loading = false
                }
              })
              this.loading = false
            })
            .catch(() => {
              this.loading = false
            })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },
    getOtherQuery(query) {
      return Object.keys(query).reduce((acc, cur) => {
        if (cur !== 'redirect') {
          acc[cur] = query[cur]
        }
        return acc
      }, {})
    },
    localLogin(info) {
      this.loading = true
      const sendStr = {
        router: 'Login',
        JsonData: { name: info.account, password: this.$md5(info.password), ip: '', terminal: 'pc' }
      }
      this.$store.dispatch('user/login', sendStr)
        .then(() => {
          this.$pomelo.conn((err, res) => {
            if (err) console.error(err)
            if (res.code === 200) {
              this.$Global.Pass = info.password
              this.$router.push({ path: this.redirect || '/', query: this.otherQuery })
              this.loading = false
            }
          })
          this.loading = false
        })
        .catch(() => {
          this.loading = false
        })
    }
  }
}
</script>

